                <p class="text-justify">
                    Desde os anos 2000, os setores da saúde e da educação no país mobilizam-se para a definição de Diretrizes Curriculares Nacionais para os cursos, inserindo-os na consolidação do Sistema Único de Saúde (SUS) e na atualização dos processos de formação, atendendo aos novos desafios da contemporaneidade na produção de saberes e práticas profissionais. Esses desafios incluem o desenvolvimento de competências para o trabalho em equipe, considerando a complexidade dos problemas de saúde e necessidade de integração de saberes e práticas interdisciplinares/interprofissionais que a constituem.
                </p>
                <p class="text-justify">
                    Em 2007, a UFRGS aprovou junto ao Ministério da Saúde o Projeto de Extensão “Integralidade na Saúde”. Ao longo da execução desse projeto, observou-se a necessidade de uma ação institucional mais propositiva, que favorecesse a discussão e a ação acadêmica com forte conexão às políticas de saúde. Em 2008 cria-se a Coordenadoria da Saúde (CoorSaúde), órgão colegiado da administração acadêmica central responsável pela integração na formação, de modo especial na construção da integralidade e da multiprofissionalidade nas atividades de ensino, pesquisa e extensão.
                </p>
                <p class="text-justify">
                    Como estratégia adicional de constituição de um espaço de integração de docentes e estudantes dos 14 cursos da área da saúde, bem como trabalhadores do Sistema Único de Saúde (SUS), foi proposta uma atividade de ensino em formato piloto denominada ‘Práticas Integradas em Saúde I’, tendo como cenário de prática o território do Distrito Glória-Cruzeiro-Cristal do SUS em Porto Alegre. O diferencial proposto foi a livre inserção dos estudantes em qualquer momento do itinerário curricular e a abordagem interdisciplinar. A estratégia consistiu na elaboração de um dispositivo capaz de integrar discentes e docentes dos diferentes cursos nos cenários de prática, superando a reprodução de modelos e rompendo com as metodologias tradicionais vigentes.
