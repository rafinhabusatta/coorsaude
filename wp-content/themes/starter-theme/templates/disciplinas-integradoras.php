                <h3 class="text-uppercase px-2 p-md-0 font-weight-bold">
                    <strong>Como está organizada?</strong>
                </h3>
                <p class="text-justify">
                    As atividades estão organizadas com momentos de concentração (toda turma reunida) e momentos de mediação com a prática/ tutoria no cotidiano do território.
                </p>
                <p class="text-justify">
                    Estes momentos cumprem respectivamente a função de integralizar as análises e reflexões à luz das teorias e discutir as especificidades de cada território no contexto do Distrito. No trabalho de campo utiliza-se a observação participante, à qual se agregam entrevistas, fotoetnografia, análise de documentos e mapeamento qualitativo georeferenciado do território, com uso de ferramentas virtuais e impressões em papel.
                </p>
                <p class="text-justify">
                    Nos momentos de concentração acontece a apresentação das atividades do semestre e do território, por meio de visitas coletivas aos serviços e à gestão distrital. Também acontecem atividades de reflexão coletiva a partir das vivências no cotidiano. Como estratégias pedagógicas, privilegia-se, ainda, a exposição dialogada e dinâmica de problematização em pequenos grupos com estudantes das diferentes tutorias. Os momentos de tutoria são desenvolvidos em USF do Distrito Glória-Cruzeiro-Cristal (80% da carga horária) por grupos com dois professores tutores e oito estudantes, ambos de cursos diferentes.
                </p>
                <p class="text-justify">
                    Os professores trabalham de forma integrada, sendo responsáveis por acompanhar quatro estudantes, o que implica: ler e dialogar com os diários de campo semanais dos estudantes, acompanhar a construção do portfólio individual e as competências constituídas por esses estudantes ao longo do semestre.
                </p>
                <p class="text-justify">
                    O processo metodológico incorporado pela ‘Práticas Integradas’ é o da problematização e avaliação do processo e produto da experiência. As competências relacionais e do trabalho em equipe são trabalhadas ao longo do semestre tendo a vivência local como dispositivo de aprendizagem.
                </p>
                
                <h3 class="text-uppercase px-2 p-md-0 font-weight-bold">
                    <strong>Processo de avaliação das aprendizagens:</strong>
                </h3>
                <p class="text-justify">
                    A avaliação se dá pelo acompanhamento da construção de um portfólio individual (com entrega parcial e final), com a descrição e análise da experiência e pela inserção dos estudantes nas atividades desenvolvidas. Informativos, jornais, pôsteres, mapas ou relatórios podem ser constituídos em parceria com as equipes de saúde a cada semestre.
