        </div>
        <div class="col-12 px-md-0">
            <p class="text-justify mb-0">
                A Coordenadoria da Saúde (CoorSaúde) da UFRGS é resultado do movimento de implantação das Diretrizes Curriculares Nacionais para a área da saúde (Edital SESu/MEC nº 4, de 10/12/1997 e Resoluções CNE/CES nos 3, 4 e 5/2001; 2, 3, 4, 5 e 6/2002; 1 e 2/2003 e 7 e 8/2004) e das Diretrizes Constitucionais Nacionais para o setor da saúde (Artigos 200, 205, 208 e 214,  da Constituição Federal de 1988), conforme desdobramentos da Lei de Diretrizes e Bases da Educação Nacional (Artigos 1º, 2º, 3º, 43 e 53) e da Lei Orgânica da Saúde (Artigos 13, 14, 15 e 27), além do consequente processo de mudança nos cursos da área.<span id="dots-historic">...</span>

                <span id="more-historic" class="d-none">
                    Dentre as principais estratégias de atenção aos determinantes legais e orientações normativas está a necessidade de ações conjuntas e integradas entre os cursos. A partir de 2004 e em resposta à convocação nacional despertada pelos Ministérios da Educação e da Saúde designado por “AprenderSUS – o SUS e os cursos de graduação da área da saúde”, os cursos da área e afins passaram a discutir, por meio de grupos de estudos ou projetos correspondentes aos editais nacionais apresentados pela Política Nacional de Formação e Desenvolvimento para o Sistema Único de Saúde: Caminhos para a Educação Permanente em Saúde (Resolução CNS nº 335, 27/11/2003) a construção da integralidade da atenção à saúde, o trabalho em equipes multiprofissionais e interdisciplinares, a apropriação do sistema de saúde vigente em nosso país e a educação permanente em docência e práticas pedagógicas na saúde.

                    Ao longo dos anos de 2006 e 2007 a UFRGS construiu, submeteu e aprovou junto ao Ministério da Saúde o Projeto de Extensão “Integralidade na saúde”. Este projeto foi intermediado pela Organização Pan-Americana da Saúde e desenvolvido com a participação de estudantes e professores dos cursos de graduação em Enfermagem, Educação Física, Farmácia, Medicina, Nutrição, Odontologia e Psicologia. Ao longo da execução do projeto, os professores envolvidos constataram a urgência de uma ação institucional que favorecesse e ampliasse a discussão e a ação acadêmica regular e oficial.

                    A Pró-Reitoria de Graduação, em decorrência das políticas de incentivo à mudança nos cursos de graduação como a do AprenderSUS e de programas de apoio como o “Pró-Saúde – Programa de Incentivo à Mudança nos Cursos de Graduação da Área da Saúde” (edital conjunto dos Ministérios da saúde e da Educação) e do REUNI (Reestruturação e expansão das Universidades Federais), envolveu-se em reuniões e debates que estimularam a proposição de uma Coordenadoria Acadêmica que pudesse tornar mais orgânica, no interior da universidade, a discussão de uma política institucional de formação na área da saúde e ações coletivas de integração entre os setores da educação e da saúde tendo em vista qualificar a formação, a atenção, a gestão e a participação nesse campo.

                    A Coordenadoria da Saúde da UFRGS surgiu, portanto, como uma proposta para contribuir com a definição de uma política inovadora, mas fundamentalmente contemporânea, de formação de profissionais da área da saúde ou com forte atuação no setor da saúde, no âmbito da Universidade e dos Serviços, tendo em vista implementar as Diretrizes Curriculares Nacionais em concordância com as políticas públicas dos setores da Educação Superior e do Sistema Único de Saúde, de modo especial a construção da integralidade e da interprofissionalidade nas atividades de ensino, pesquisa e extensão.
